# escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milimetros
medida = int (input('digite uma medida em metros, que vou converter em Centimetros e milimetros'))
cm = medida * 100
mm = medida *1000
print (f'{medida} metros em centimetros é igual a: {cm} centimetros' )
print (f'{medida} metros em milimetros é igual a: {mm} milimetros')

       
       
       


