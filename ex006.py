 # crie um algoritimo, que leia um numero e mostre o seu dobro, triplo e raiz quadrada
numero = int(input ("digite um numero que vou te mostrar, qual o dobro, o triplo e a raiz quadrada"))
dobro = numero * 2
triplo = numero * 3
raiz = numero ** 2
print (f'o dobro de {numero} é {dobro} o triplo de {numero} é {triplo} a raiz quadrada de {numero} é {raiz}')