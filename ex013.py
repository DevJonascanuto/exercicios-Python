# faça um algoritimo que leia o salario de um funcionario e mostre seu novo salario com 15% de aumento
salario = float (input ('digite seu salario'))
aumentoSalario = float (salario * 0.15)
novoSalario =  ( salario + aumentoSalario)
print (f'o novo salario do funcionario com aumento é {novoSalario:.2f}')
 
